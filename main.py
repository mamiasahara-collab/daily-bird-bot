def select_bird_for_date(date_str: str) -> str:
    hash_val = int(hashlib.md5(date_str.encode()).hexdigest(), 16)
    index = hash_val % len(BIRDS)
    return BIRDS[index]


def get_today_bird_message(bird_name: str) -> str:
    client = anthropic.Anthropic(api_key=os.environ["ANTHROPIC_API_KEY"])

    message = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=600,
        messages=[
            {
                "role": "user",
                "content": f"""「{bird_name}」という鳥を紹介してください。

条件：
- 以下のフォーマットで、200字前後で書いてください
- フォーマット以外の余分な文章は一切追加しないでください

フォーマット：
🐦 今日の鳥：{bird_name}
📍 生息地：[主な生息地]
✨ 特徴：[興味深い特徴を2〜3文で]""",
            }
        ],
    )

    return message.content[0].text.strip()


def send_line_message(text: str) -> None:
    token = os.environ["LINE_CHANNEL_ACCESS_TOKEN"]
    user_id = os.environ["LINE_USER_ID"]

    url = "https://api.line.me/v2/bot/message/push"
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {token}",
    }
    payload = {
        "to": user_id,
        "messages": [{"type": "text", "text": text}],
    }

    response = requests.post(url, json=payload, headers=headers, timeout=10)

    if response.status_code != 200:
        raise RuntimeError(
            f"LINE送信エラー: ステータスコード {response.status_code}\n"
            f"詳細: {response.text}"
        )


def main():
    today = datetime.now(JST).strftime("%Y-%m-%d")
    bird_name = select_bird_for_date(today)
    print(f"[{today}] 今日の鳥：{bird_name}")

    try:
        message = get_today_bird_message(bird_name)
    except anthropic.APIError as e:
        print(f"Claude APIエラー: {e}", file=sys.stderr)
        sys.exit(1)

    print("生成されたメッセージ:")
    print(message)
    print()

    try:
        send_line_message(message)
        print("LINEへの送信が完了しました！")
    except RuntimeError as e:
        print(f"LINE送信失敗: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()
